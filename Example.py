def Multiplication(arg1, arg2):
    """
    Multiplication of arg1 and arg2, return multiplied value

    :param arg1: some value

    :type arg1:  float

    :param arg2: some value

    :type arg2: float

    :return:

    :rtype: float

    """
    mul = arg1*arg2
    return mul


def division(arg1, arg2):
    """
    Return the division of arg1 and arg2
    :param arg1:
    :type arg1:
    :param arg2:
    :type arg2:
    :return:
    :rtype:
    """
    div = arg1/arg2
    return div